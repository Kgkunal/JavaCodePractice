public class Relational {
    public static void main(String[] args){
        int a = 10;
        int b = 20;
        System.out.println("Value of a is ="+ a +"\nValue of b is =" + b);

        System.out.println("a == b : " + (a == b));
        System.out.println("a != b : " + (a != b));
        System.out.println("a > b : " + (a > b));
        System.out.println("a < b : " + (a < b));
        System.out.println("a <= b : " + (a <= b));
        System.out.println("a >= b : " + (a >= b));
        
    }
}
